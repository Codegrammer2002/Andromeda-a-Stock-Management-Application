    private void getProfit(){
        int qty = Integer.parseInt(qtyF.getText());
        int profit = Integer.parseInt(profitF.getText());
        int total_profit = qty * profit;
        String str_profit = String.valueOf(total_profit);
       totalProfitF.setText(str_profit);
        
    }
    private void addToProfit(){
    
    try{
            String sql = "Insert into ProfitInfo(SalesID,SalesItem, Profit,Quantity,TotalProfit,Date) values (?,?,?,?,?,?) ";
            String Item_name = SoldItem_Box.getSelectedItem().toString();
                String quantity = qtyF.getText();
                String profit = profitF.getText();
                String total = totalProfitF.getText();
                
                String date = dateF.getText();
                
            ps = conn.prepareStatement(sql);
            ps.setString(1, IDF.getText());
            ps.setString(2, Item_name);
           
            ps.setString(3, profit);
            ps.setString(4, quantity);
            ps.setString(5, total);
            ps.setString(6, date);
        
            ps.execute();
            JOptionPane.showMessageDialog(null, "Data saved!");
      
        }
        catch(Exception e){
            
                        JOptionPane.showMessageDialog(null, e);

        }
     finally {
            try{
                rs.close();
                ps.close();
    
              }
            catch(Exception e) {
                   }
      }
    }
